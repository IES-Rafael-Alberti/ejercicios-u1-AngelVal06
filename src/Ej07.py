# Escribe un programa que solicite tres números al usuario y calcule e imprima por pantalla su suma.

num1 = int(input("Dime el primer número"))

num2 = int(input("Dime el segundo número"))

num3 = int(input("Dime el tercer número"))

num4 = num1 + num2 + num3

print(f"La suma de los tres números es {num4}")